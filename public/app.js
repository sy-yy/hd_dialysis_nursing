/**
 * 处理注册
 */
async function handleRegister() {
  showAuthError('reg-error');
  const successEl = document.getElementById('reg-success');
  if (successEl) successEl.classList.add('hidden');
  
  const username = document.getElementById('reg-user').value.trim();
  const password = document.getElementById('reg-pass').value;
  const displayName = document.getElementById('reg-name').value.trim();
  
  if (!username || !password || !displayName) {
    showAuthError('reg-error', '请填写所有必填项');
    return;
  }
  
  if (username.length < 2) {
    showAuthError('reg-error', '用户名至少2个字符');
    return;
  }
  
  if (password.length < 6) {
    showAuthError('reg-error', '密码至少6位');
    return;
  }
  
  try {
    setButtonBusy('btn-register', true, '提交中...', '提交注册（待审核）');
    
    const r = await fetch(apiUrl('/api/auth/register'), {
      method: 'POST',
      credentials: 'include',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ username, password, displayName }),
    });
    
    const data = await r.json().catch(() => ({}));
    
    if (!r.ok) {
      showAuthError('reg-error', data.error || '注册失败');
      return;
    }
    
    if (successEl) {
      successEl.textContent = data.message || '已提交，请等待管理员审核';
      successEl.classList.remove('hidden');
    }
    
    // 清空表单
    document.getElementById('reg-name').value = '';
    document.getElementById('reg-user').value = '';
    document.getElementById('reg-pass').value = '';
    
  } catch (e) {
    const m = e && e.message ? String(e.message) : '';
    showAuthError('reg-error', m === 'Failed to fetch' || m.includes('NetworkError') ? networkErrorMessage() : m);
  } finally {
    setButtonBusy('btn-register', false, '提交中...', '提交注册（待审核）');
  }
}

