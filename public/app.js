/**
 * 刷新管理员用户列表
 */
async function refreshAdminUsers() {
  if (!me || me.role !== 'admin') return;
  
  try {
    const users = await api('/api/admin/users');
    const pending = users.filter(u => u.role === 'doctor' && u.status === 'pending');
    const doctors = users.filter(u => u.role === 'doctor');
    
    // 渲染待审核列表
    const pb = document.getElementById('admin-pending-body');
    if (pb) {
      pb.innerHTML = '';
      
      if (!pending.length) {
        pb.innerHTML = '<tr><td colspan="4" class="empty-hint">暂无待审核申请</td></tr>';
      } else {
        pending.forEach(u => {
          const tr = document.createElement('tr');
          tr.innerHTML = `
            <td>${escapeHtml(u.username)}</td>
            <td>${escapeHtml(u.displayName || '')}</td>
            <td>${formatDate(u.createdAt, false)}</td>
            <td>
              <button type="button" class="btn btn-approve" data-id="${u.id}">通过</button>
              <button type="button" class="btn secondary btn-reject" data-id="${u.id}">拒绝</button>
            </td>
          `;
          pb.appendChild(tr);
        });
      }
      
      // 绑定待审核按钮事件
      pb.onclick = async (e) => {
        const apBtn = e.target.closest('.btn-approve');
        const rjBtn = e.target.closest('.btn-reject');
        
        if (apBtn) {
          const uid = apBtn.getAttribute('data-id');
          try {
            await api(`/api/admin/users/${uid}/approve`, { method: 'POST' });
            showMessage('审核通过', 'success');
            refreshAdminUsers();
          } catch (e) {
            showMessage('操作失败: ' + e.message, 'error');
          }
        }
        
        if (rjBtn) {
          if (!confirm('确定拒绝该注册申请？')) return;
          const uid = rjBtn.getAttribute('data-id');
          try {
            await api(`/api/admin/users/${uid}/reject`, { method: 'POST' });
            showMessage('已拒绝', 'success');
            refreshAdminUsers();
          } catch (e) {
            showMessage('操作失败: ' + e.message, 'error');
          }
        }
      };
    }
    
    // 渲染医师权限列表
    const ub = document.getElementById('admin-users-body');
    if (ub) {
      ub.innerHTML = '';
      
      doctors.forEach(u => {
        const tr = document.createElement('tr');
        tr.setAttribute('data-admin-user', u.id);
        
        // 解析权限JSON字符串（后端返回的是字符串）
        const p = (typeof u.permissions === 'string' ? JSON.parse(u.permissions) : u.permissions) || {};
        const cells = PERM_KEYS.map(key => 
          `<td class="tc"><input type="checkbox" data-p="${key}" ${p[key] !== false ? 'checked' : ''} ${u.status !== 'active' ? 'disabled' : ''}></td>`
        ).join('');
        
        const isActive = u.status === 'active';
        
        tr.innerHTML = `
          <td>${escapeHtml(u.username)}</td>
          <td>${escapeHtml(u.displayName || '')}</td>
          <td>${u.status === 'pending' ? '待审核' : u.status === 'active' ? '正常' : u.status === 'rejected' ? '已拒绝' : '无效'}</td>
          <td class="tc">
            <input type="checkbox" class="user-active-checkbox" data-id="${u.id}" ${isActive ? 'checked' : ''}>
          </td>
          ${cells}
          <td>
            <button type="button" class="btn secondary btn-save-perm" data-id="${u.id}">保存权限</button>
            <button type="button" class="btn secondary btn-reset-password" data-id="${u.id}">重置密码</button>
            <button type="button" class="btn secondary btn-change-password" data-id="${u.id}" ${u.status !== 'active' ? 'disabled' : ''}>修改密码</button>
            <button type="button" class="btn danger btn-del-user" data-id="${u.id}" ${u.status !== 'active' ? 'disabled' : ''} style="margin-left: 4px;">删除账号</button>
          </td>
        `;
        
        ub.appendChild(tr);
      });
      
      // 绑定权限管理按钮事件
      ub.onclick = async (e) => {
        const saveBtn = e.target.closest('.btn-save-perm');
        const resetBtn = e.target.closest('.btn-reset-password');
        const changeBtn = e.target.closest('.btn-change-password');
        
        if (saveBtn && !saveBtn.disabled) {
          const uid = saveBtn.getAttribute('data-id');
          const row = ub.querySelector(`tr[data-admin-user="${uid}"]`);
          
          if (!row) return;
          
          const permissions = {};
          PERM_KEYS.forEach(key => {
            const inp = row.querySelector(`input[data-p="${key}"]`);
            permissions[key] = !!(inp && inp.checked);
          });
          
          const activeCheckbox = row.querySelector('.user-active-checkbox');
          const isChecked = activeCheckbox?.checked;
          const newStatus = isChecked ? 'active' : 'inactive';
          
          try {
            await api(`/api/admin/users/${uid}`, {
              method: 'PATCH',
              body: JSON.stringify({ 
                permissions, 
                status: newStatus 
              })
            });
            showMessage('权限和启用状态已保存', 'success');
            refreshAdminUsers();
          } catch (e) {
            showMessage('保存失败:' + e.message, 'error');
          }
        }
        
        if (resetBtn) {
          const uid = resetBtn.getAttribute('data-id');
          if (!confirm('确定要将该用户的密码重置为 123456 吗？')) return;
          
          try {
            await api(`/api/admin/users/${uid}/reset-password`, { method: 'POST' });
            showMessage('密码已重置为 123456', 'success');
          } catch (e) {
            showMessage('重置失败: ' + e.message, 'error');
          }
        }
        
        if (changeBtn && !changeBtn.disabled) {
          const uid = changeBtn.getAttribute('data-id');
          const newPassword = prompt('请输入新密码：');
          
          if (newPassword === null) return; // 取消操作
          if (newPassword.trim() === '') {
            showMessage('密码不能为空', 'warning');
            return;
          }
          
          try {
            await api(`/api/admin/users/${uid}/change-password`, { 
              method: 'POST', 
              body: JSON.stringify({ newPassword: newPassword.trim() }) 
            });
            showMessage('密码修改成功', 'success');
          } catch (e) {
            showMessage('修改密码失败: ' + e.message, 'error');
          }
        }
        
        // 删除用户账号
        const delUserBtn = e.target.closest('.btn-del-user');
        if (delUserBtn && !delUserBtn.disabled) {
          const uid = delUserBtn.getAttribute('data-id');
          const username = delUserBtn.closest('tr')?.cells[0]?.textContent || '该用户';
          
          if (!confirm(`确定删除 ${username} 的账号吗？此操作不可恢复！`)) return;
          
          try {
            await api(`/api/admin/users/${uid}`, { method: 'DELETE' });
            showMessage('用户账号已删除', 'success');
            refreshAdminUsers();
          } catch (e) {
            showMessage('删除失败: ' + e.message, 'error');
          }
        }
      };
    }
  } catch (e) {
    console.error('刷新管理员用户列表失败:', e);
    showMessage('刷新用户列表失败: ' + e.message, 'error');
  }
}

