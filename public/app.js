/**
 * 提交评分
 */
async function submitScore() {
  const isA = me && me.role === 'admin';
  const canS = isA || (me && me.permissions && me.permissions.scorePatients !== false);
  
  if (!canS) {
    showMessage('无权限进行评分', 'error');
    return;
  }
  
  const pid = document.getElementById('score-patient-id')?.value;
  if (!pid) {
    showMessage('请选择患者', 'warning');
    return;
  }
  
  await loadRubric();
  
  const answers = {};
  let unanswered = [];
  
  for (const d of (rubricCache.dimensions || [])) {
    if (!window.examAnswers?.[d.id]) {
      unanswered.push(d.name || d.id);
    } else {
      answers[d.id] = window.examAnswers[d.id];
    }
  }
  
  if (unanswered.length > 0) {
    showMessage(`请完成以下评分项目：${unanswered.join('、')}`, 'warning');
    return;
  }
  
  // 检查是否修改了自动选择的答案，并验证选项是否符合实际计算值
  const patientInfo = window.patientAssessmentInfo || {};
  const patientName = patientInfo.name || '该患者';
  const ageRangeDesc = patientInfo.ageRangeDescription || '';
  const dialysisAgeRangeDesc = patientInfo.dialysisAgeRangeDescription || '';
  const firstDialysisDesc = patientInfo.firstDialysisDescription || '';
  
  // 收集所有需要检查不一致性的项目
  const allItemsToCheck = [];
  const modifiedItems = [];
  
  // 1. 检查修改了自动填充答案的项目
  if (window.autoSelectedDefaults) {
    for (const [dimId, autoDefault] of Object.entries(window.autoSelectedDefaults)) {
      if (answers[dimId] && answers[dimId] !== autoDefault.itemId) {
        // 用户修改了自动选择的答案
        const dimension = rubricCache.dimensions?.find(d => d.id === dimId);
        if (dimension) {
          const autoItem = dimension.items?.find(i => i.id === autoDefault.itemId);
          const selectedItem = dimension.items?.find(i => i.id === answers[dimId]);
          if (autoItem && selectedItem) {
            const itemInfo = {
              dimension: dimension.name,
              autoLabel: autoItem.label,
              selectedLabel: selectedItem.label,
              itemId: answers[dimId],
              isAge: dimId === 'age',
              isDialysisAge: dimId === 'dialysis_age',
              isFirstDialysis: dimId === 'first_dialysis',
              modified: true
            };
            modifiedItems.push(itemInfo);
            allItemsToCheck.push(itemInfo);
          }
        }
      }
    }
  }
  
  // 2. 检查所有年龄和透析龄选项（即使没有自动填充）
  const ageItemId = answers['age'];
  const dialysisAgeItemId = answers['dialysis_age'];
  
  if (ageItemId) {
    // 检查是否已经在 modifiedItems 中
    const alreadyChecked = allItemsToCheck.some(item => item.isAge);
    if (!alreadyChecked) {
      const dimension = rubricCache.dimensions?.find(d => d.id === 'age');
      if (dimension) {
        const selectedItem = dimension.items?.find(i => i.id === ageItemId);
        if (selectedItem) {
          allItemsToCheck.push({
            dimension: dimension.name,
            selectedLabel: selectedItem.label,
            itemId: ageItemId,
            isAge: true,
            isDialysisAge: false,
            modified: false
          });
        }
      }
    }
  }
  
  if (dialysisAgeItemId) {
    // 检查是否已经在 allItemsToCheck 中
    const alreadyChecked = allItemsToCheck.some(item => item.isDialysisAge);
    if (!alreadyChecked) {
      const dimension = rubricCache.dimensions?.find(d => d.id === 'dialysis_age');
      if (dimension) {
        const selectedItem = dimension.items?.find(i => i.id === dialysisAgeItemId);
        if (selectedItem) {
          allItemsToCheck.push({
            dimension: dimension.name,
            selectedLabel: selectedItem.label,
            itemId: dialysisAgeItemId,
            isAge: false,
            isDialysisAge: true,
            isFirstDialysis: false,
            modified: false
          });
        }
      }
    }
  }
  
  // 3. 检查首次血液透析选项（即使没有自动填充）
  const firstDialysisItemId = answers['first_dialysis'];
  
  if (firstDialysisItemId) {
    // 检查是否已经在 allItemsToCheck 中
    const alreadyChecked = allItemsToCheck.some(item => item.isFirstDialysis);
    if (!alreadyChecked) {
      const dimension = rubricCache.dimensions?.find(d => d.id === 'first_dialysis');
      if (dimension) {
        const selectedItem = dimension.items?.find(i => i.id === firstDialysisItemId);
        if (selectedItem) {
          allItemsToCheck.push({
            dimension: dimension.name,
            selectedLabel: selectedItem.label,
            itemId: firstDialysisItemId,
            isAge: false,
            isDialysisAge: false,
            isFirstDialysis: true,
            modified: false
          });
        }
      }
    }
  }
  
  // 4. 检查每个选项是否与实际计算值一致
  const inconsistentItems = [];
  for (const item of allItemsToCheck) {
    const isConsistent = checkOptionConsistency(item.itemId, item.isAge, item.isDialysisAge, item.isFirstDialysis);
    if (!isConsistent) {
      inconsistentItems.push(item);
    }
  }
  
  // 4. 构建提示信息
  if (modifiedItems.length > 0 || inconsistentItems.length > 0) {
    let details = '';
    
    // 如果有修改，显示修改详情
    if (modifiedItems.length > 0) {
      const modificationDetails = modifiedItems.map(m => 
        `\n• ${m.dimension}：自动「${m.autoLabel}」→ 您选择了「${m.selectedLabel}」`
      ).join('');
      details += `您修改了以下自动填充的答案：${modificationDetails}`;
    }
    
    // 如果有不一致的选项，添加警告
    if (inconsistentItems.length > 0) {
      if (details) details += '\n';
      const inconsistentDetails = inconsistentItems.map(m => {
        if (m.isAge) {
          return `\n  ⚠️ ${patientName}实际年龄为${ageRangeDesc}，您选择的「${m.selectedLabel}」与实际年龄不符`;
        } else if (m.isDialysisAge) {
          return `\n  ⚠️ ${patientName}实际透析龄为${dialysisAgeRangeDesc}，您选择的「${m.selectedLabel}」与实际透析龄不符`;
        } else if (m.isFirstDialysis) {
          return `\n  ⚠️ ${patientName}实际透析情况为${firstDialysisDesc}，您选择的「${m.selectedLabel}」与实际不符`;
        }
        return '';
      }).join('');
      details += `以下选项与实际计算值不符：${inconsistentDetails}`;
    }
    
    details += '\n\n是否确认提交？';
    
    if (!confirm(details)) {
      return; // 用户取消提交
    }
  }
  
  try {
    setButtonBusy('btn-submit-score', true, '保存中...', '保存本次评估');
    
    const remark = document.getElementById('score-remark')?.value?.trim() || '';
    
    const res = await api(`/api/patients/${pid}/assessments`, {
      method: 'POST',
      body: JSON.stringify({ answers, remark })
    });
    
    showMessage('评估保存成功', 'success');
    
    // 刷新数据
    refreshPatients();
    refreshDashboard();
    
    // 跳转到患者列表
    show('patients');
    
  } catch (e) {
    showMessage('保存失败: ' + e.message, 'error');
  } finally {
    setButtonBusy('btn-submit-score', false, '保存中...', '保存本次评估');
  }
}

