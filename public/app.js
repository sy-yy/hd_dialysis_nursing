/**
 * 通用API请求函数
 * @param {string} path - API路径
 * @param {Object} opts - fetch选项
 * @returns {Promise<any>} 响应数据
 */
async function api(path, opts = {}) {
  const controller = new AbortController();
  const timeoutId = setTimeout(() => controller.abort(), CONFIG.API_TIMEOUT);

  try {
    const response = await fetch(apiUrl(path), {
      credentials: 'include',
      headers: { 'Content-Type': 'application/json', ...(opts.headers || {}) },
      signal: controller.signal,
      ...opts,
    });

    clearTimeout(timeoutId);

    const data = await response.json().catch(() => ({}));
    
    if (!response.ok) {
      throw new Error(data.error || `请求失败: ${response.statusText || response.status}`);
    }
    
    return data;
  } catch (error) {
    clearTimeout(timeoutId);
    
    const msg = error && error.message ? String(error.message) : '';
    if (msg.includes('Failed to fetch') || msg.includes('NetworkError') || msg.includes('Load failed') || error.name === 'AbortError') {
      throw new Error(error.name === 'AbortError' ? '请求超时，请稍后重试' : networkErrorMessage());
    }
    throw error;
  }
}

