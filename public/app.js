/**
 * 刷新系统设置
 */
async function refreshSettings() {
  if (!me || (me.role !== 'admin' && (!me.permissions || me.permissions.systemSettings === false))) {
    show('dashboard');
    return;
  }
  
  // 渲染评分级别设置
  const levelsList = document.getElementById('settings-levels-list');
  if (levelsList) {
    levelsList.innerHTML = '';
    
    try {
      await loadRubric(true);
      
      rubricCache.levelRules?.forEach(l => {
        const item = document.createElement('div');
        item.className = 'level-item';
        item.style.cssText = 'padding: 12px; border: 1px solid #e8eaf6; border-radius: 8px; margin-bottom: 12px; background: #f8f9fa;';
        
        item.innerHTML = `
          <div style="display: flex; justify-content: space-between; align-items: center; margin-bottom: 8px;">
            <h3 style="margin: 0; font-size: 1rem;">${escapeHtml(l.name)}</h3>
            <button type="button" class="btn secondary btn-edit-level" data-id="${l.id}" style="padding: 6px 12px; font-size: 0.85rem;">编辑</button>
          </div>
          <p style="margin: 4px 0; font-size: 0.9rem; color: #666;">范围：${l.minScore} - ${l.maxScore} 分</p>
          <p style="margin: 4px 0; font-size: 0.9rem; color: #666;">周期：${l.defaultFrequencyDays} 天</p>
          <p style="margin: 4px 0; font-size: 0.9rem; color: #666;">
            颜色：<span style="display:inline-block; width:16px; height:16px; border-radius:50%; background:${l.color}; vertical-align: middle;"></span> ${l.color}
          </p>
        `;
        
        levelsList.appendChild(item);
      });
      
      // 绑定编辑级别按钮事件
      levelsList.onclick = async (e) => {
        const btn = e.target.closest('.btn-edit-level');
        if (!btn) return;
        
        const l = rubricCache.levelRules?.find(x => x.id === btn.getAttribute('data-id'));
        if (!l) return;
        
        const name = prompt('名称', l.name);
        if (!name) return;
        
        const min = parseInt(prompt('最低分', l.minScore));
        const max = parseInt(prompt('最高分', l.maxScore));
        const freq = parseInt(prompt('周期（天）', l.defaultFrequencyDays));
        const color = prompt('颜色', l.color);
        
        if (isNaN(min) || isNaN(max) || isNaN(freq)) {
          showMessage('请输入有效的数字', 'warning');
          return;
        }
        
        try {
          await api(`/api/rubric/levels/${l.id}`, {
            method: 'PUT',
            body: JSON.stringify({ 
              name, 
              minScore: min, 
              maxScore: max, 
              defaultFrequencyDays: freq, 
              color 
            })
          });
          showMessage('更新成功', 'success');
          await refreshSettings();
          // 刷新评估提醒数据
          await refreshReminders();
        } catch (e) {
          showMessage('更新失败: ' + e.message, 'error');
        }
      };
    } catch (e) {
      console.error('加载评分级别失败:', e);
    }
  }
}

