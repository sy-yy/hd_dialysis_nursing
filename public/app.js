/**
 * 获取API基础URL
 * @returns {string} API基础URL
 */
function getApiBase() {
  if (typeof window === 'undefined') return '';
  if (window.__HD_API_BASE__ != null && String(window.__HD_API_BASE__).trim() !== '') {
    return String(window.__HD_API_BASE__).replace(/\/$/, '');
  }
  if (location.protocol === 'file:') return 'http://127.0.0.1:3081';
  const p = location.port;
  const offPort = ['5500', '5501', '8080', '5173', '4173', '3000', '4200'];
  if (p && offPort.includes(p)) return 'http://127.0.0.1:3081';
  return '';
}

