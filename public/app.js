/**
 * 刷新评估提醒
 */
async function refreshReminders() {
  try {
    // 添加时间戳防止缓存
    const res = await api('/api/reminders?_t=' + Date.now());
    let assessments = [];
    
    try {
      assessments = await api('/api/assessments?_t=' + Date.now());
    } catch (e) {
      console.error('获取评估记录失败:', e);
    }
    
    // 构建每个患者的最新评估映射
    const latestAssessmentMap = {};
    assessments.forEach(a => {
      const existing = latestAssessmentMap[a.patientId];
      if (!existing || new Date(a.assessedAt) > new Date(existing.assessedAt)) {
        latestAssessmentMap[a.patientId] = a;
      }
    });
    
    // 渲染逾期表格
    const tb1 = document.getElementById('rem-overdue-body');
    if (tb1) {
      tb1.innerHTML = '';
      
      if (!res.overdue?.length) {
        tb1.innerHTML = '<tr><td colspan="5" class="empty-hint">无逾期记录</td></tr>';
      } else {
        res.overdue.forEach(r => {
          const la = latestAssessmentMap[r.patient?.id];
          const tr = document.createElement('tr');
          // 优先使用患者的最新评估级别（已从后端重新计算），其次使用评估记录中的级别
          const currentLevelId = r.patient?.lastLevelId || (la ? la.levelId : '');
          tr.innerHTML = `
            <td>${escapeHtml(r.patient?.name || '—')}</td>
            <td>${escapeHtml(levelDisplayName(currentLevelId))}</td>
            <td>${la ? la.totalScore : '—'}</td>
            <td class="tag-overdue">${formatDate(r.dueAt)}</td>
            <td>逾期${r.daysLeft > 0 ? r.daysLeft : Math.abs(r.daysLeft)}天</td>
          `;
          tb1.appendChild(tr);
        });
      }
    }
    
    // 渲染待评表格
    const tb2 = document.getElementById('rem-upcoming-body');
    if (tb2) {
      tb2.innerHTML = '';
      
      if (!res.upcoming?.length) {
        tb2.innerHTML = '<tr><td colspan="5" class="empty-hint">暂无计划内待评</td></tr>';
      } else {
        res.upcoming.forEach(r => {
          const la = latestAssessmentMap[r.patient?.id];
          const tr = document.createElement('tr');
          // 优先使用患者的最新评估级别（已从后端重新计算），其次使用评估记录中的级别
          const currentLevelId = r.patient?.lastLevelId || (la ? la.levelId : '');
          tr.innerHTML = `
            <td>${escapeHtml(r.patient?.name || '—')}</td>
            <td>${escapeHtml(levelDisplayName(currentLevelId))}</td>
            <td>${la ? la.totalScore : '—'}</td>
            <td>${formatDate(r.dueAt)}</td>
            <td>剩余${r.daysLeft}天</td>
          `;
          tb2.appendChild(tr);
        });
      }
    }
  } catch (e) {
    console.error('刷新评估提醒失败:', e);
    showMessage('刷新评估提醒失败: ' + e.message, 'error');
  }
}

