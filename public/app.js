/**
 * 刷新评估记录
 */
async function refreshRecords() {
  const searchValue = document.getElementById('records-search')?.value?.toLowerCase() || '';
  const levelFilter = document.getElementById('records-filter-level')?.value || '';
  
  try {
    const assessments = await api('/api/assessments');
    const patients = await api('/api/patients');
    const rubric = await loadRubric();
    
    // 构建患者映射
    const patientMap = {};
    patients.forEach(p => patientMap[p.id] = p);
    
    // 处理评估记录
    let filtered = assessments.map(a => {
      const patient = patientMap[a.patientId] || {};
      const level = rubric.levelRules?.find(l => 
        a.totalScore >= l.minScore && a.totalScore <= (l.maxScore || 999)
      ) || {};
      
      return {
        ...a,
        patientName: patient.name || '未知',
        patientGender: patient.gender || '—',
        patientBirthDate: patient.birthDate || '',
        doctorName: a.doctorName || a.assessorName || '未知',
        levelName: level.name || '未知',
        levelId: level.id || '',
      };
    });
    
    // 应用筛选
    if (searchValue) {
      filtered = filtered.filter(r => 
        matchPatient(searchValue, patientMap[r.patientId] || { name: '' })
      );
    }
    
    if (levelFilter) {
      filtered = filtered.filter(r => r.levelId === levelFilter);
    }
    
    // 按时间排序
    filtered.sort((a, b) => new Date(b.assessedAt) - new Date(a.assessedAt));
    
    // 分页
    const totalPages = Math.ceil(filtered.length / CONFIG.PAGE_SIZE);
    if (recordsPage > totalPages) recordsPage = totalPages || 1;
    if (recordsPage < 1) recordsPage = 1;
    
    const pageData = filtered.slice(
      (recordsPage - 1) * CONFIG.PAGE_SIZE,
      recordsPage * CONFIG.PAGE_SIZE
    );
    
    const tbody = document.getElementById('records-body');
    if (!tbody) return;
    
    tbody.innerHTML = '';
    
    // 检查删除权限（管理员或拥有deleteRecords权限的用户）
    const isAdmin = me && me.role === 'admin';
    const canDelete = isAdmin || (me && me.permissions && me.permissions.deleteRecords !== false);
    
    // 显示/隐藏批量删除按钮和复选框列
    const batchDeleteBtn = document.getElementById('btn-batch-delete-records');
    const checkCol = document.getElementById('records-check-col');
    if (batchDeleteBtn) batchDeleteBtn.style.display = canDelete ? 'inline-block' : 'none';
    if (checkCol) checkCol.style.display = canDelete ? '' : 'none';
    
    if (!pageData.length) {
      tbody.innerHTML = '<tr><td colspan="' + (canDelete ? 9 : 8) + '" class="empty-hint">暂无评估记录</td></tr>';
    } else {
      pageData.forEach(r => {
        const tr = document.createElement('tr');
        const checkBoxHtml = canDelete ? `<td><input type="checkbox" class="record-checkbox" data-id="${r.id}" aria-label="选择此记录" /></td>` : '';
        tr.innerHTML = `
          ${checkBoxHtml}
          <td>${escapeHtml(r.patientName)}</td>
          <td>${escapeHtml(r.patientGender)}</td>
          <td>${calculateAge(r.patientBirthDate)}</td>
          <td>${formatDate(r.assessedAt)}</td>
          <td>${r.totalScore || 0}</td>
          <td>${escapeHtml(r.levelName)}</td>
          <td>${escapeHtml(r.doctorName)}</td>
          <td>
            <button type="button" class="btn secondary" onclick="viewAssessment('${r.id}')">查看</button>
            ${canDelete ? `<button type="button" class="btn danger btn-del-record" data-id="${r.id}" style="margin-left: 4px;">删除</button>` : ''}
          </td>
        `;
        tbody.appendChild(tr);
      });
    }
    
    renderRecordsPagination(totalPages);
    
    // 绑定全选事件
    const checkAll = document.getElementById('records-check-all');
    if (checkAll) {
      checkAll.onchange = function() {
        const checkboxes = tbody.querySelectorAll('.record-checkbox');
        checkboxes.forEach(cb => cb.checked = this.checked);
        updateBatchDeleteButton();
      };
    }
    
    // 绑定复选框事件
    if (tbody) {
      tbody.onchange = function(e) {
        if (e.target.classList.contains('record-checkbox')) {
          updateBatchDeleteButton();
        }
      };
    }
    
    // 绑定删除按钮事件
    if (tbody) {
      tbody.onclick = async function(e) {
        const delBtn = e.target.closest('.btn-del-record');
        if (delBtn) {
          const recordId = delBtn.getAttribute('data-id');
          if (!recordId) return;
          
          if (!confirm('确定删除该评估记录？此操作不可恢复！')) return;
          
          try {
            await api(`/api/assessments/${recordId}`, { method: 'DELETE' });
            showMessage('删除成功', 'success');
            refreshRecords();
            refreshDashboard();
            refreshPatients();  // 刷新患者数据，确保分级评估信息更新
          } catch (e) {
            showMessage('删除失败: ' + e.message, 'error');
          }
        }
      };
    }
    
  } catch (e) {
    console.error('加载评估记录失败:', e);
    showMessage('加载评估记录失败: ' + e.message, 'error');
  }
}

