/**
 * 严格校验身份证号码
 * @param {string} idCard - 身份证号
 * @returns {Object} { valid: boolean, error: string|null }
 */
function validateIdCardStrict(idCard) {
  // 空值不校验（允许为空）
  if (!idCard || idCard.trim() === '') {
    return { valid: true, error: null };
  }
  
  const id = idCard.trim();
  
  // 1. 检查位数是否为18位
  if (id.length !== 18) {
    return { 
      valid: false, 
      error: `身份证号码位数不正确：当前${id.length}位，应为18位。请输入正确的18位身份证号码。` 
    };
  }
  
  // 2. 基本格式校验（18位，前17位为数字，最后一位可以是数字或X/x）
  const basicReg = /^\d{17}[\dXx]$/;
  if (!basicReg.test(id)) {
    return { 
      valid: false, 
      error: '身份证号码格式不正确。前17位应为数字，最后一位为数字或X（不区分大小写）。' 
    };
  }
  
  // 3. 提取出生年月
  let birthYear, birthMonth, birthDay;
  try {
    birthYear = parseInt(id.substring(6, 10), 10);
    birthMonth = parseInt(id.substring(10, 12), 10);
    birthDay = parseInt(id.substring(12, 14), 10);
    
    // 检查是否为有效日期
    if (isNaN(birthYear) || isNaN(birthMonth) || isNaN(birthDay)) {
      return { 
        valid: false, 
        error: '无法从身份证号码中提取有效的出生年月。请检查身份证号码是否正确。' 
      };
    }
    
    // 检查月份和日期范围
    if (birthMonth < 1 || birthMonth > 12) {
      return { 
        valid: false, 
        error: '身份证号码中的出生月份无效（应为1-12）。请检查身份证号码是否正确。' 
      };
    }
    
    if (birthDay < 1 || birthDay > 31) {
      return { 
        valid: false, 
        error: '身份证号码中的出生日期无效（应为1-31）。请检查身份证号码是否正确。' 
      };
    }
    
    // 构造出生日期并验证
    const birthDate = new Date(birthYear, birthMonth - 1, birthDay);
    if (birthDate.getFullYear() !== birthYear || 
        birthDate.getMonth() !== birthMonth - 1 || 
        birthDate.getDate() !== birthDay) {
      return { 
        valid: false, 
        error: '身份证号码中的出生日期不存在（如2月30日）。请检查身份证号码是否正确。' 
      };
    }
  } catch (e) {
    return { 
      valid: false, 
      error: '无法从身份证号码中提取出生年月。请检查身份证号码是否正确。' 
    };
  }
  
  // 4. 检查出生日期是否大于当前日期
  const today = new Date();
  today.setHours(0, 0, 0, 0); // 忽略时间部分
  const birthDateObj = new Date(birthYear, birthMonth - 1, birthDay);
  
  if (birthDateObj > today) {
    return { 
      valid: false, 
      error: `身份证号码中的出生日期（${birthYear}-${String(birthMonth).padStart(2, '0')}-${String(birthDay).padStart(2, '0')}）晚于当前日期，不符合常理。请重新输入。` 
    };
  }
  
  // 5. 计算年龄并检查是否大于120岁
  let age = today.getFullYear() - birthYear;
  if (today.getMonth() < birthMonth - 1 || 
      (today.getMonth() === birthMonth - 1 && today.getDate() < birthDay)) {
    age--;
  }
  
  if (age > 120) {
    return { 
      valid: false, 
      error: `根据身份证号码计算出的年龄为${age}岁，超过合理范围（最大120岁）。请检查身份证号码是否正确。` 
    };
  }
  
  // 6. 校验码校验（第18位）
  const factor = [7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2];
  const checkCodes = ['1', '0', 'X', '9', '8', '7', '6', '5', '4', '3', '2'];
  let sum = 0;
  
  for (let i = 0; i < 17; i++) {
    sum += parseInt(id.charAt(i), 10) * factor[i];
  }
  
  const checkCode = checkCodes[sum % 11];
  const lastChar = id.charAt(17).toUpperCase();
  
  if (lastChar !== checkCode) {
    return { 
      valid: false, 
      error: `身份证号码校验码不正确（最后一位应为"${checkCode}"）。请检查身份证号码是否正确。` 
    };
  }
  
  return { valid: true, error: null };
}

/**
 * 根据身份证号计算年龄
 * @param {string} id - 身份证号
 * @returns {number|null} 年龄或null
 */
function getAgeFromIdCard(id) {
  if (!id || (id.length !== 15 && id.length !== 18)) return null;
  const birthYear = id.length === 15 ? '19' + id.substring(6, 8) : id.substring(6, 10);
  const birthMonth = id.length === 15 ? id.substring(8, 10) : id.substring(10, 12);
  const birthDay = id.length === 15 ? id.substring(10, 12) : id.substring(12, 14);
  
  const birthDate = new Date(`${birthYear}-${birthMonth}-${birthDay}`);
  if (isNaN(birthDate.getTime())) return null;
  
  const today = new Date();
  let age = today.getFullYear() - birthDate.getFullYear();
  
  if (today.getMonth() < birthDate.getMonth() || 
      (today.getMonth() === birthDate.getMonth() && today.getDate() < birthDate.getDate())) {
    age--;
  }
  
  return age >= 0 ? age : null;
}

/**
 * 根据出生日期计算年龄
 * @param {string} b - 出生日期
 * @returns {number|null} 年龄或null
 */
function getAgeFromBirthDate(b) {
  if (!b) return null;
  try {
    const bd = new Date(b);
    if (isNaN(bd.getTime())) return null;
    
    const today = new Date();
    let age = today.getFullYear() - bd.getFullYear();
    
    if (today.getMonth() < bd.getMonth() || 
        (today.getMonth() === bd.getMonth() && today.getDate() < bd.getDate())) {
      age--;
    }
    
    return age >= 0 ? age : null;
  } catch (e) {
    return null;
  }
}

/**
 * 自动计算并显示年龄
 * 从出生日期或身份证号计算年龄，并显示在年龄字段中
 */
function updateAgeDisplay() {
  const pmBirth = document.getElementById('pm-birth');
  const pmIdCard = document.getElementById('pm-idcard');
  const pmAge = document.getElementById('pm-age');
  
  if (!pmAge) return;
  
  let age = null;
  
  // 优先使用出生日期
  if (pmBirth && pmBirth.value) {
    age = getAgeFromBirthDate(pmBirth.value);
  }
  
  // 如果出生日期无效，尝试从身份证号计算
  if (age === null && pmIdCard && pmIdCard.value) {
    const idCard = pmIdCard.value.trim();
    if (idCard.length === 15 || idCard.length === 18) {
      age = getAgeFromIdCard(idCard);
    }
  }
  
  pmAge.value = age !== null ? age : '';
}

/**
 * 计算透析年龄（年）
 * @param {string} d - 首次透析日期
 * @returns {number} 透析年龄（年）
 */
function getDialysisAgeFromDate(d) {
  if (!d) return 0;
  try {
    const dialysisDate = new Date(d);
    if (isNaN(dialysisDate.getTime())) return 0;
    
    return (new Date() - dialysisDate) / (1000 * 60 * 60 * 24 * 365.25);
  } catch (e) {
    return 0;
  }
}

/**
 * 检查用户选择的选项是否与实际计算的年龄/透析龄一致
 * @param {string} itemId - 选择的选项ID
 * @param {boolean} isAge - 是否为年龄选项
 * @param {boolean} isDialysisAge - 是否为透析龄选项
 * @param {boolean} isFirstDialysis - 是否为首次血液透析选项
 * @returns {boolean} 是否一致
 */
function checkOptionConsistency(itemId, isAge, isDialysisAge, isFirstDialysis) {
  const patientInfo = window.patientAssessmentInfo;
  if (!patientInfo) return true; // 无法验证，默认一致
  
  if (isAge) {
    // 从年龄范围描述中提取数值
    // 格式如："57岁（中年，45~<60岁）" 或 "15岁（儿童/未成年）"
    const ageRangeDesc = patientInfo.ageRangeDescription || '';
    const ageMatch = ageRangeDesc.match(/(\d+)岁/);
    if (!ageMatch) return true;
    const actualAge = parseInt(ageMatch[1], 10);
    
    // 年龄选项范围：
    // age_0: 18~<45岁（青年）
    // age_1: 45~<60岁（中年）
    // age_4: ≥60岁（老年）或<18岁（儿童）
    switch (itemId) {
      case 'age_0':
        return actualAge >= 18 && actualAge < 45;
      case 'age_1':
        return actualAge >= 45 && actualAge < 60;
      case 'age_4':
        return actualAge >= 60 || actualAge < 18;
      default:
        return true;
    }
  }
  
  if (isDialysisAge) {
    // 从透析龄范围描述中提取数值
    // 格式如："2.5年（1~<3年）" 或 "7.3年（5~10年）"
    const dialysisRangeDesc = patientInfo.dialysisAgeRangeDescription || '';
    const dialysisMatch = dialysisRangeDesc.match(/([\d.]+)年/);
    if (!dialysisMatch) return true;
    const actualYears = parseFloat(dialysisMatch[1]);
    
    // 透析龄选项范围：
    // da_0: <1年
    // da_1: 1~<3年
    // da_2: 3~<5年
    // da_3: 5~10年
    // da_4: >10年
    switch (itemId) {
      case 'da_0':
        return actualYears < 1;
      case 'da_1':
        return actualYears >= 1 && actualYears < 3;
      case 'da_2':
        return actualYears >= 3 && actualYears < 5;
      case 'da_3':
        return actualYears >= 5 && actualYears <= 10;
      case 'da_4':
        return actualYears > 10;
      default:
        return true;
    }
  }
  
  if (isFirstDialysis) {
    // 从首次透析描述中提取信息
    // 格式如："是（透析龄 < 3个月）" 或 "否（透析龄 ≥ 3个月）"
    const fdDesc = patientInfo.firstDialysisDescription || '';
    const isActuallyFirst = fdDesc.includes('是');
    
    // 首次血液透析选项：
    // fd_0: 否 (0分)
    // fd_4: 是 (4分)
    switch (itemId) {
      case 'fd_0':
        return !isActuallyFirst; // 选择"否"，实际不是首次，才一致
      case 'fd_4':
        return isActuallyFirst; // 选择"是"，实际是首次，才一致
      default:
        return true;
    }
  }
  
  return true; // 未知类型，默认一致
}

