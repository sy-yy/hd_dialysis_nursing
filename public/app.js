/**
 * 渲染评估记录分页
 * @param {number} totalPages - 总页数
 */
function renderRecordsPagination(totalPages) {
  const pagination = document.getElementById('records-pagination');
  if (!pagination) return;
  
  pagination.innerHTML = '';
  
  if (totalPages <= 1) return;
  
  // 上一页按钮
  const prevBtn = document.createElement('button');
  prevBtn.className = 'btn secondary';
  prevBtn.textContent = '上一页';
  prevBtn.disabled = recordsPage <= 1;
  prevBtn.onclick = () => { 
    recordsPage--; 
    refreshRecords(); 
  };
  pagination.appendChild(prevBtn);
  
  // 页码按钮
  const maxVisiblePages = 5;
  let startPage = Math.max(1, recordsPage - Math.floor(maxVisiblePages / 2));
  let endPage = Math.min(totalPages, startPage + maxVisiblePages - 1);
  
  if (endPage - startPage < maxVisiblePages - 1) {
    startPage = Math.max(1, endPage - maxVisiblePages + 1);
  }
  
  for (let i = startPage; i <= endPage; i++) {
    const btn = document.createElement('button');
    btn.className = `btn ${i === recordsPage ? '' : 'secondary'}`;
    btn.textContent = i;
    btn.onclick = () => { 
      recordsPage = i; 
      refreshRecords(); 
    };
    pagination.appendChild(btn);
  }
  
  // 下一页按钮
  const nextBtn = document.createElement('button');
  nextBtn.className = 'btn secondary';
  nextBtn.textContent = '下一页';
  nextBtn.disabled = recordsPage >= totalPages;
  nextBtn.onclick = () => { 
    recordsPage++; 
    refreshRecords(); 
  };
  pagination.appendChild(nextBtn);
}

