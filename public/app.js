/**
 * 导出评估记录
 */
async function exportRecords() {
  try {
    const assessments = await api('/api/assessments');
    const patients = await api('/api/patients');
    const rubric = await loadRubric();
    
    const patientMap = {};
    patients.forEach(p => patientMap[p.id] = p);
    
    const data = assessments.map(a => {
      const patient = patientMap[a.patientId] || {};
      const level = rubric.levelRules?.find(l => 
        a.totalScore >= l.minScore && a.totalScore <= (l.maxScore || 999)
      ) || {};
      
      return {
        '患者姓名': patient.name || '未知',
        '性别': patient.gender || '—',
        '年龄': calculateAge(patient.birthDate),
        '评估时间': formatDate(a.assessedAt, false),
        '评估分数': a.totalScore || 0,
        '评估分级': level.name || '未知',
        '评估医师': a.doctorName || a.assessorName || '未知',
      };
    });
    
    data.sort((a, b) => new Date(b['评估时间']) - new Date(a['评估时间']));
    
    // 生成CSV
    let csv = '\uFEFF' + Object.keys(data[0] || {}).join(',') + '\n';
    data.forEach(row => {
      csv += Object.values(row).map(v => `"${v}"`).join(',') + '\n';
    });
    
    const blob = new Blob([csv], { type: 'text/csv;charset=utf-8;' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `评估记录_${new Date().toISOString().slice(0, 10)}.csv`;
    a.click();
    URL.revokeObjectURL(url);
    
    showMessage('导出成功', 'success');
  } catch (e) {
    showMessage('导出失败: ' + e.message, 'error');
  }
}

